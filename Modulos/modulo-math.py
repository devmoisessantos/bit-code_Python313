# MODULO MATH:
"""
- O modulo math possui diversas funcoes matematicas
- Ex: raiz quadrada, potencia, logaritmo, etc...

É bastante utilizado para trabalhar com numero, por exemplo:

"""

import math

# 1 - Raiz quadrada:
raiz = math.sqrt(9)

# 2 - Exponencial:
print(math.exp(3))

# 3 - Potência:
potencia = math.pow(2, 3)

# 4 - Logaritmo:
log = math.log(10, 2)

# 5 - Coseno:
print(math.cos(30))

# 6 - Seno:
calc_seno = math.sin(30)

# 6 - Tangente:
print(math.tan(30))

# 7 - Arredondamento:
arredondar = math.ceil(3.14)

# 8 - Raiz cubica:
raiz_cubica = math.cbrt(27)

# 9 - Pi:
print(math.pi)

# 10 - Valor absoluto:
print(math.fabs(-10))